# ============================================================
# 14. WHAT ARE THE AVERAGE PRICES OF DIFFERENT TYPES OF MUSIC?
# ============================================================
WITH PURCHASES AS
	(SELECT
		GENRE.NAME AS GENRE,
        SUM(TOTAL) AS TOTAL_SPENT
	FROM INVOICE
    JOIN INVOICE_LINE
    ON INVOICE.INVOICE_ID = INVOICE_LINE.INVOICE_ID
    JOIN TRACK
    ON TRACK.TRACK_ID = INVOICE_LINE.TRACK_ID
    JOIN GENRE
    ON GENRE.GENRE_ID = TRACK.GENRE_ID
    GROUP BY
		GENRE
	ORDER BY
		TOTAL_SPENT)
SELECT
	GENRE,
    CONCAT('$', ROUND(AVG(TOTAL_SPENT))) AS TOTAL_SPENT
FROM PURCHASES
GROUP BY
	GENRE;
    
    
